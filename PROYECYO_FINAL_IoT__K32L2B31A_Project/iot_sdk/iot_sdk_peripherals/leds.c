void led_on_green(){
	//Encender led verde
	GPIO_PinWrite(GPIOD,5,0);
}

void led_off_green(){
	//Apagar led verde
	GPIO_PinWrite(GPIOD,5,1);
}
/****************************************************************/
void led_on_red(){
	//Encender led verde
	GPIO_PinWrite(GPIOE,31,0);
}

void led_off_red(){
	//Apagar led verde
	GPIO_PinWrite(GPIOE,31,1);
}
